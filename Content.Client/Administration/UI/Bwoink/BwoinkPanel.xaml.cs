        private void RenderTriageBar(NetUserId channel, string category)
        {
            // Label
            var label = new FormattedMessage();
            label.PushColor(Color.LightGray);
            label.AddText(Loc.GetString("bwoink-triage-suggested-prefix"));
            label.AddText(" ");
            label.Pop();
            label.PushColor(Color.Goldenrod);
            label.AddText(category);
            label.Pop();
            TriageCategoryLabel.SetMessage(label);

            // Buttons
            TriageActionContainer.RemoveAllChildren();
            AddAlwaysOnShortcuts(channel);

            var console = IoCManager.Resolve<IClientConsoleHost>();
            var channelArg = $"\"{channel}\"";

            // Category-specific shortcuts. Mapping is intentionally conservative;
            // each shortcut points to a command that is known to exist server-side.
            foreach (var extra in GetCategoryShortcuts(category))
            {
                var localized = Loc.GetString(extra.LocaleKey);
                var command = extra.CommandFactory(channelArg);
                TriageActionContainer.AddChild(MakeShortcutButton(localized,
                    () => console.ExecuteCommand(command)));
            }

            TriageBar.Visible = true;
        }

        private static Button MakeShortcutButton(string text, Action onPressed)
        {
            var button = new Button
            {
                Text = text,
                StyleClasses = { "OpenLeft" },
            };
            button.OnPressed += _ => onPressed();
            return button;
        }

        private readonly record struct TriageShortcut(string LocaleKey, Func<string, string> CommandFactory);

        private void OnPlayerSnapshotReceived(PlayerSnapshotResponseMessage response)
        {
            if (Disposed || _adminChannelId is not { } channel)
                return;

            if (!string.Equals(response.OwnerUserId, channel.UserId.ToString(), StringComparison.Ordinal))
                return;

            var window = new TriageInfoWindow(Loc.GetString("bwoink-triage-popup-snapshot-title"));

            if (!string.IsNullOrEmpty(response.Error) || response.Snapshot is null)
            {
                window.AddMarkup($"[color=salmon]{Loc.GetString("bwoink-triage-snapshot-error")}[/color]");
                window.OpenCentered();
                return;
            }

            var s = response.Snapshot;
            window.AddMarkup($"[color=goldenrod]{Loc.GetString("bwoink-triage-snapshot-header")}[/color]");

            var statusChip = s.Online ? "[color=lightgreen]online[/color]" : "[color=salmon]offline[/color]";
            var mindChip = s.HasMind ? "[color=lightgreen]has mind[/color]" : "[color=salmon]no mind[/color]";
            var detachedChip = s.DetachedFromBody ? " [color=orange](detached from body)[/color]" : string.Empty;
            var attachedDisplay = string.IsNullOrEmpty(s.AttachedEntityName) ? "—" : s.AttachedEntityName;

            window.AddMarkup($"  status: {statusChip}, {mindChip}{detachedChip}");
            window.AddMarkup($"  attached: [color=white]{attachedDisplay}[/color]");
            window.AddMarkup($"  location: {s.MapName} @ ({s.WorldPosition.X:F0}, {s.WorldPosition.Y:F0})");

            if (s.Roles.Length > 0)
                window.AddMarkup($"  roles: [color=cyan]{string.Join(", ", s.Roles)}[/color]");

            window.OpenCentered();
        }

        private static IEnumerable<TriageShortcut> GetCategoryShortcuts(string category)
        {
            // Match against the well-known default category names. Custom categories
            // get just the always-on Player Panel button.
            switch (category.Trim().ToLowerInvariant())
            {
                case "respawn":
                    yield return new TriageShortcut(
                        "bwoink-triage-action-respawn",
                        arg => $"respawn {arg}");
                    break;
                case "stuck":
                case "lost":
                    yield return new TriageShortcut(
                        "bwoink-triage-action-follow",
                        arg => $"follow {arg}");
                    break;
                case "harassment":
                case "grief":
                    yield return new TriageShortcut(
                        "bwoink-triage-action-notes",
                        arg => $"adminnotes {arg}");
                    break;
            }
        }

        private void OnPlayerBankInfoReceived(PlayerBankInfoResponseMessage response)
        {
            if (Disposed || _adminChannelId is not { } channel)
                return;

            if (!string.Equals(response.OwnerUserId, channel.UserId.ToString(), StringComparison.Ordinal))
                return;

            if (!string.IsNullOrEmpty(response.Error))
            {
                var window = new TriageInfoWindow(Loc.GetString("bwoink-triage-popup-banking-title"));
                window.AddMarkup($"[color=salmon]{Loc.GetString("bwoink-triage-banking-error")}[/color]");
                window.OpenCentered();
