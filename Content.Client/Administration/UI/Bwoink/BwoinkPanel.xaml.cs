        private void OpenAssignDeedWindow(NetUserId channel)
        {
            if (_assignDeedWindow == null || _assignDeedWindow.Disposed || !_assignDeedWindow.IsOpen)
            {
                _assignDeedWindow = new AssignDeedWindow(channel.UserId.ToString());
                _assignDeedWindow.OnClose += () => _assignDeedWindow = null;
                _assignDeedWindow.RefreshRequested += () =>
                {
                    var system = IoCManager.Resolve<IEntityManager>().System<BwoinkSystem>();
                    system.RequestShipDeedList(channel);
                };
                _assignDeedWindow.AssignRequested += ship =>
                {
                    var system = IoCManager.Resolve<IEntityManager>().System<BwoinkSystem>();
                    system.RequestAssignShipDeed(channel, ship.ShipNetEntity);
                };
                _assignDeedWindow.OpenCentered();
            }
            else
            {
                _assignDeedWindow.MoveToFront();
            }

            // Always request a fresh list when opening / focusing.
            var bwoinkSys = IoCManager.Resolve<IEntityManager>().System<BwoinkSystem>();
            bwoinkSys.RequestShipDeedList(channel);
        }

        private void OnShipDeedListReceived(ShipDeedListResponseMessage response)
