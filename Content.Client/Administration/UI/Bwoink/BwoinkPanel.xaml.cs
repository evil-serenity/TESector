            window.AddMarkup($"Spawned [color=goldenrod]{FormattedMessage.EscapeText(itemName)}[/color] ([color=lightgray]{FormattedMessage.EscapeText(response.PrototypeId)}[/color]) at ({response.SpawnPosition.X:F0}, {response.SpawnPosition.Y:F0}).");
            window.OpenCentered();
        }

        private string RenderItemMentions(SharedBwoinkSystem.BwoinkTextMessage message)
        {
            if (!EnsureItemLookup())
                return message.Text;

            var split = message.Text.IndexOf(':');
            if (split < 0 || split >= message.Text.Length - 1)
                return message.Text;

            var bodyStart = split + 1;
            while (bodyStart < message.Text.Length && char.IsWhiteSpace(message.Text[bodyStart]))
            {
                bodyStart++;
            }

            if (bodyStart >= message.Text.Length)
                return message.Text;

            var prefix = message.Text[..bodyStart];
            var body = message.Text[bodyStart..];
            var ownerArg = message.UserId.UserId.ToString();

            var matches = ItemWordRegex.Matches(body);
            if (matches.Count == 0)
                return message.Text;

            var renderedBody = new StringBuilder(body.Length + 32);
            var cursor = 0;

            for (var index = 0; index < matches.Count;)
            {
                var maxLen = Math.Min(3, matches.Count - index);
                var bestLen = 0;
                string? bestKeyword = null;

                for (var len = maxLen; len >= 1; len--)
                {
                    if (!CanMergePhrase(matches, index, len, body))
                        continue;

                    var normalized = BuildMergedToken(matches, index, len);
                    if (!_itemKeywordToPrototypes.ContainsKey(normalized))
                        continue;

                    bestLen = len;
                    bestKeyword = normalized;
                    break;
                }

                if (bestLen == 0 || bestKeyword == null)
                {
                    index++;
                    continue;
                }

                var first = matches[index];
                var last = matches[index + bestLen - 1];
                var phraseStart = first.Index;
                var phraseEnd = last.Index + last.Length;

                if (phraseStart > cursor)
                    renderedBody.Append(body, cursor, phraseStart - cursor);

                var phraseText = body[phraseStart..phraseEnd];
                var safePhrase = FormattedMessage.EscapeText(phraseText);
                // Open a chooser even when the keyword resolves to a single
                // candidate so the admin always confirms which item they meant.
                var command = $"ahelpitemmenu {ownerArg} {bestKeyword}";
                // cmdlink uses the tag's Value as the rendered text, NOT inner text.
                // Correct form: [cmdlink="label" command="cmd"/]
                renderedBody.Append($"[cmdlink=\"{safePhrase}\" command=\"{command}\"/]");

                cursor = phraseEnd;
                index += bestLen;
            }

            if (cursor < body.Length)
                renderedBody.Append(body, cursor, body.Length - cursor);

            return prefix + renderedBody;
        }

        private static bool CanMergePhrase(MatchCollection matches, int start, int len, string source)
        {
            for (var i = start; i < start + len - 1; i++)
            {
                var current = matches[i];
                var next = matches[i + 1];
                var gapStart = current.Index + current.Length;
                var gapLength = next.Index - gapStart;

                if (gapLength < 0)
                    return false;

                for (var g = 0; g < gapLength; g++)
                {
                    var ch = source[gapStart + g];
                    if (!char.IsWhiteSpace(ch) && ch != '-' && ch != '_')
                        return false;
                }
            }

            return true;
        }

        private static string BuildMergedToken(MatchCollection matches, int start, int len)
        {
            var sb = new StringBuilder();
            for (var i = start; i < start + len; i++)
            {
                var normalized = NormalizeItemToken(matches[i].Value);
                if (normalized.Length > 0)
                    sb.Append(normalized);
            }

            return sb.ToString();
        }

        private bool EnsureItemLookup()
        {
            var currentProtoCount = _prototype.Count<EntityPrototype>();
            if (_itemLookupBuilt &&
                _itemLookupPrototypeCount == currentProtoCount &&
                _itemKeywordToPrototypes.Count > 0)
            {
                return true;
            }

            _itemLookupPrototypeCount = currentProtoCount;
            _itemKeywordToPrototypes.Clear();
            AhelpItemMentionRegistry.Clear();

            foreach (var proto in _prototype.EnumeratePrototypes<EntityPrototype>())
            {
                if (proto.Abstract || !IsItemPrototype(proto))
                    continue;

                AddItemKeyword(proto.ID, proto.ID);
                AddItemKeyword(proto.Name, proto.ID);

                if (!string.IsNullOrWhiteSpace(proto.EditorSuffix))
                    AddItemKeyword(proto.EditorSuffix, proto.ID);
            }

            foreach (var (keyword, ids) in _itemKeywordToPrototypes)
                AhelpItemMentionRegistry.Set(keyword, ids);

            _itemLookupBuilt = _itemKeywordToPrototypes.Count > 0;
            return _itemLookupBuilt;
        }

        private bool IsItemPrototype(EntityPrototype proto)
        {
            if (proto.HasComponent<ItemComponent>())
                return true;

            if (proto.Parents == null || proto.Parents.Length == 0)
                return false;

            var seen = new HashSet<string> { proto.ID };
            var queue = new Queue<string>(proto.Parents);

            while (queue.TryDequeue(out var parentId))
            {
                if (!seen.Add(parentId))
                    continue;

                if (!_prototype.TryIndex(parentId, out var parent))
                    continue;

                if (parent.HasComponent<ItemComponent>())
                    return true;

                if (parent.Parents == null || parent.Parents.Length == 0)
                    continue;

                foreach (var grandParent in parent.Parents)
                {
                    queue.Enqueue(grandParent);
                }
            }

            return false;
        }

        private void AddItemKeyword(string key, string prototypeId)
        {
            if (string.IsNullOrWhiteSpace(key))
                return;

            var normalized = NormalizeItemToken(key);
            AddItemKeywordAlias(normalized, prototypeId);

            var tokens = ExtractKeywordTokens(key);
            if (tokens.Count > 0)
                AddItemKeywordAlias(tokens[^1], prototypeId);

            if (tokens.Count >= 2)
            {
                var lastTwo = tokens[^2] + tokens[^1];
                AddItemKeywordAlias(lastTwo, prototypeId);

                // Some IDs encode "MagBoots" as "...BootsMag"; support both orders.
                if (tokens[^1].Length <= 4)
                {
                    var reversed = tokens[^1] + tokens[^2];
                    AddItemKeywordAlias(reversed, prototypeId);
                }
            }
        }

        private void AddItemKeywordAlias(string normalized, string prototypeId)
        {
            if (normalized.Length < 3)
                return;

            if (!_itemKeywordToPrototypes.TryGetValue(normalized, out var bucket))
            {
                bucket = new List<string>();
                _itemKeywordToPrototypes[normalized] = bucket;
            }

            if (bucket.Count >= MaxCandidatesPerKeyword || bucket.Contains(prototypeId))
                return;

            bucket.Add(prototypeId);
        }

        private static List<string> ExtractKeywordTokens(string source)
        {
            var tokens = new List<string>();
            var sb = new StringBuilder();
            var previousWasLower = false;

            void Flush()
            {
                if (sb.Length == 0)
                    return;

                var token = sb.ToString();
                sb.Clear();

                if (token.Length >= 3)
                    tokens.Add(token);
            }

            foreach (var ch in source)
            {
                if (!char.IsLetterOrDigit(ch))
                {
                    Flush();
                    previousWasLower = false;
                    continue;
                }

                if (char.IsLetter(ch) && char.IsUpper(ch) && previousWasLower)
                    Flush();

                var lowered = char.ToLowerInvariant(ch);
                sb.Append(lowered);
                previousWasLower = char.IsLower(lowered);
            }

            Flush();
            return tokens;
        }

        private static string NormalizeItemToken(string token)
        {
            var sb = new StringBuilder(token.Length);
            foreach (var ch in token)
            {
                if (char.IsLetterOrDigit(ch))
                    sb.Append(char.ToLowerInvariant(ch));
            }

            return sb.ToString();
        }

        private void OnPlayerShipInspectionReceived(PlayerShipInspectionResponseMessage response)
        {
            if (Disposed || _adminChannelId is not { } channel)
                return;

            // Match this panel's channel (server returns the OwnerUserId we sent).
            if (!string.Equals(response.OwnerUserId, channel.UserId.ToString(), StringComparison.Ordinal))
                return;

            var window = new TriageInfoWindow(Loc.GetString("bwoink-triage-popup-ships-title"));

            if (!string.IsNullOrEmpty(response.Error))
            {
                window.AddMarkup($"[color=salmon]{Loc.GetString("bwoink-triage-ship-inspect-error")}[/color]");
                window.OpenCentered();
                return;
            }

            if (response.Ships.Length == 0)
            {
                window.AddMarkup($"[color=lightgray]{Loc.GetString("bwoink-triage-ship-inspect-none")}[/color]");
                window.OpenCentered();
                return;
            }

            window.AddMarkup($"[color=goldenrod]{Loc.GetString("bwoink-triage-ship-inspect-header", ("count", response.Ships.Length))}[/color]");

            foreach (var ship in response.Ships)
            {
                var displayName = string.IsNullOrEmpty(ship.Suffix) ? ship.Name : $"{ship.Name} ({ship.Suffix})";
                var ftlChip = ship.InFtl
                    ? $"[color=orange]FTL: {ship.FtlState}[/color]"
                    : $"[color=lightgreen]FTL: {ship.FtlState}[/color]";
                var onlineChip = ship.OwnerOnline
                    ? "[color=lightgreen]online[/color]"
                    : "[color=salmon]offline[/color]";
                var voucherChip = ship.PurchasedWithVoucher ? " [color=cyan](voucher)[/color]" : string.Empty;
                window.AddMarkup(
                    $"  [color=white]{displayName}[/color]{voucherChip} — {ship.MapName} @ ({ship.WorldPosition.X:F0}, {ship.WorldPosition.Y:F0}) — {ftlChip} — owner {onlineChip}");
            }

            window.OpenCentered();
        }

        private void RefreshTriageFromCache()
        {
            if (Disposed || _adminChannelId is null)
                return;

            // If the cached message no longer classifies under the new config, hide.
            if (_lastClassifiedText is { } text)
                UpdateTriageBar(text, force: true);
        }

        private void UpdateTriageBar(string messageText, bool force = false)
        {
            if (_adminChannelId is not { } channel)
                return;

            if (!force && string.Equals(_lastClassifiedText, messageText, StringComparison.Ordinal))
                return;

            _lastClassifiedText = messageText;

            var bwoinkSystem = IoCManager.Resolve<IEntityManager>().System<BwoinkSystem>();
            var category = AhelpCategoryClassifier.Classify(messageText, bwoinkSystem.CurrentAhelpAdminConfig);

            if (category is null)
            {
                if (_lastClassifiedCategory != null)
                {
                    _lastClassifiedCategory = null;
                    ShowAlwaysOnShortcuts(channel);
                }
                return;
            }

            // Skip rebuilding buttons if the category didn't change.
            if (_lastClassifiedCategory == category)
                return;

            _lastClassifiedCategory = category;
            RenderTriageBar(channel, category);
        }

