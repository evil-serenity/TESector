        private SharedMacroWindow? _sharedMacroWindow;

        private void OpenSharedMacroWindow()
        {
            if (_sharedMacroWindow == null || _sharedMacroWindow.Disposed || !_sharedMacroWindow.IsOpen)
            {
                _sharedMacroWindow = new SharedMacroWindow();
                _sharedMacroWindow.OnClose += () => _sharedMacroWindow = null;
                _sharedMacroWindow.RefreshRequested += () => _bwoinkSystem.RequestSharedAdminMacros();
                _sharedMacroWindow.CopySelectedToLocalRequested += CopySharedMacroToLocal;
                _sharedMacroWindow.CopyLocalToSharedRequested += CopyLocalMacroToShared;
                _sharedMacroWindow.DeleteSharedRequested += DeleteSelectedSharedMacro;
                _sharedMacroWindow.UpdateMacros(_bwoinkSystem.CurrentSharedAdminMacros);
                _sharedMacroWindow.OpenCentered();
            }
            else
            {
                _sharedMacroWindow.MoveToFront();
            }

            _bwoinkSystem.RequestSharedAdminMacros();
        }

        private void UpdateSharedAdminMacros(SharedAdminMacroState[] macros)
        {
            _sharedMacroWindow?.UpdateMacros(macros);
        }

        private void CopySharedMacroToLocal()
        {
            if (_sharedMacroWindow?.SelectedMacro is not { } macro)
                return;

            SavedCommandName.Text = macro.Name;
            SavedCommandText.TextRope = new Rope.Leaf(macro.Command);
            UpdateSavedCommandButtons();
        }

        private void CopyLocalMacroToShared()
        {
            var name = SavedCommandName.Text.Trim();
            var command = Rope.Collapse(SavedCommandText.TextRope).Trim();
            if (name.Length == 0 || command.Length == 0)
                return;

            _bwoinkSystem.UpsertSharedAdminMacro(name, command);
        }

        private void DeleteSelectedSharedMacro()
        {
            if (_sharedMacroWindow?.SelectedMacro is not { } macro)
                return;

            _bwoinkSystem.DeleteSharedAdminMacro(macro.Name);
        }

        private static string EscapeSavedValue(string value)
        {
            return value
                .Replace("\\", "\\\\")
                .Replace("\r", "\\r")
                .Replace("\n", "\\n")
                .Replace("\t", "\\t");
        }

        private static string UnescapeSavedValue(string value)
        {
            var chars = new List<char>(value.Length);
            var escaping = false;

            foreach (var ch in value)
            {
                if (!escaping)
                {
                    if (ch == '\\')
                    {
                        escaping = true;
                        continue;
                    }

                    chars.Add(ch);
                    continue;
                }

                chars.Add(ch switch
                {
                    'r' => '\r',
                    'n' => '\n',
                    't' => '\t',
                    '\\' => '\\',
                    _ => ch,
                });

                escaping = false;
            }

            if (escaping)
                chars.Add('\\');

            return new string(chars.ToArray());
        }

        private static bool TryParseSavedCommand(string line, out SavedCommandEntry? entry)
        {
            entry = null;

            var separator = line.IndexOf('\t');
            if (separator <= 0 || separator >= line.Length - 1)
                return false;

            var name = UnescapeSavedValue(line[..separator]);
            var command = UnescapeSavedValue(line[(separator + 1)..]);

            if (string.IsNullOrWhiteSpace(name) || string.IsNullOrWhiteSpace(command))
                return false;

            entry = new SavedCommandEntry
            {
                Name = name,
                Command = command,
            };

            return true;
        }

        private void RunSavedCommand()
        {
            var selected = GetSelectedSavedIndex();
            if (selected >= 0)
            {
                RunSavedCommandAt(selected);
                return;
            }

            var command = Rope.Collapse(SavedCommandText.TextRope).Trim();
            if (command.Length == 0)
                return;

            RunCommandSequence(command);
        }

        private void SaveSavedCommand()
        {
            var name = SavedCommandName.Text.Trim();
            var command = Rope.Collapse(SavedCommandText.TextRope).Trim();
            if (name.Length == 0 || command.Length == 0)
                return;

            var existing = FindSavedIndex(name);
            if (existing >= 0)
                _savedCommands[existing].Command = command;
            else
                _savedCommands.Add(new SavedCommandEntry { Name = name, Command = command });

            PersistSavedCommands();
            RefreshSavedCommandsList();
            UpdateSavedCommandButtons();
        }

        private void DeleteSavedCommand()
        {
            var selected = GetSelectedSavedIndex();
            if (selected < 0)
            {
                // Fall back to matching by name field if nothing is selected.
                var name = SavedCommandName.Text.Trim();
                if (name.Length == 0)
                    return;
                selected = FindSavedIndex(name);
                if (selected < 0)
                    return;
            }

            _savedCommands.RemoveAt(selected);
            PersistSavedCommands();
            RefreshSavedCommandsList();
            UpdateSavedCommandButtons();
        }

        private void RefreshSavedCommandsList()
        {
            SavedCommandsList.Clear();
            foreach (var entry in _savedCommands)
            {
                SavedCommandsList.AddItem($"{entry.Name}  -  {entry.Command}");
            }
        }

        private int GetSelectedSavedIndex()
        {
            var selected = SavedCommandsList.GetSelected().FirstOrDefault();
            if (selected == null)
                return -1;
            return SavedCommandsList.IndexOf(selected);
        }

        private void OnSavedCommandSelected(int index)
        {
            if (index < 0 || index >= _savedCommands.Count)
                return;

            var selected = _savedCommands[index];
            SavedCommandName.Text = selected.Name;
            SavedCommandText.TextRope = new Rope.Leaf(selected.Command);
            UpdateSavedCommandButtons();
        }

        private int FindSavedIndex(string name)
        {
            for (var i = 0; i < _savedCommands.Count; i++)
            {
                if (string.Equals(_savedCommands[i].Name, name, System.StringComparison.OrdinalIgnoreCase))
                    return i;
            }
            return -1;
        }

        private void LoadSavedCommands()
        {
            _savedCommands.Clear();

            try
            {
                if (!_resources.UserData.Exists(SavedCommandsPath))
                    return;

                using var reader = _resources.UserData.OpenText(SavedCommandsPath);
                while (true)
                {
                    var line = reader.ReadLine();
                    if (line == null)
                        break;

                    if (string.IsNullOrWhiteSpace(line))
                        continue;

                    if (!TryParseSavedCommand(line, out var entry) || entry == null)
                        continue;

                    _savedCommands.Add(entry);
                }

                if (_savedCommands.Count > 0)
                    return;
            }
            catch (IOException)
            {
                // Ignore read errors, treat as empty list.
            }
            catch (System.Exception)
            {
                // Invalid/unsupported path or provider-specific read errors: treat as empty list.
            }
        }

        private void PersistSavedCommands()
        {
            try
            {
                using var writer = _resources.UserData.OpenWriteText(SavedCommandsPath);

                foreach (var entry in _savedCommands)
                {
                    var name = EscapeSavedValue(entry.Name);
                    var command = EscapeSavedValue(entry.Command);
                    writer.WriteLine($"{name}\t{command}");
                }
            }
            catch (IOException)
            {
                // Best-effort save; surface via console for the admin.
                _console.WriteError(null, Loc.GetString("admin-tools-saved-write-error"));
            }
            catch (System.Exception)
            {
                _console.WriteError(null, Loc.GetString("admin-tools-saved-write-error"));
            }
        }

        private void RunSavedCommandAt(int index)
        {
            if (index < 0 || index >= _savedCommands.Count)
                return;

            RunCommandSequence(_savedCommands[index].Command);
        }

        private void RunCommandSequence(string commandBlock)
        {
            foreach (var command in commandBlock
                         .Split('\n')
                         .Select(line => line.Trim())
                         .Where(line => line.Length > 0))
            {
                _console.ExecuteCommand(command);
            }
        }
    }
