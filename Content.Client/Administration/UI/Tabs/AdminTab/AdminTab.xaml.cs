        {
            var hasTitle = CustomVoteTitle.Text.Trim().Length > 0;
            var optionCount = CustomVoteOptions.Text
                .Split(';')
                .Count(x => x.Trim().Length > 0);
            CreateCustomVoteButton.Disabled = !(hasTitle && optionCount >= 2);
        }

