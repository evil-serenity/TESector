        private void UpdateAhelpEditorButtons()
        {
            var selectedCategory = SelectedCategoryState();
            var hasSelectedCategory = selectedCategory != null;
            var botName = AutoReplyBotName.Text.Trim();
            var hasCategoryName = AhelpCategoryName.Text.Trim().Length > 0;
            var hasTemplate = Rope.Collapse(AutoReplyTemplate.TextRope).Trim().Length > 0;
            var panicTemplate = Rope.Collapse(PanicTemplate.TextRope).Trim();
            var hasKeywords = Rope.Collapse(TriageKeywords.TextRope).Trim().Length > 0;

            SaveAutoReplyBotNameButton.Disabled = botName.Length == 0 || string.Equals(botName, _currentAutoReplyBotName, StringComparison.Ordinal);
            AddCategoryButton.Disabled = !(hasCategoryName && hasTemplate && hasKeywords);
            RemoveCategoryButton.Disabled = !hasSelectedCategory;
            AutoReplySaveButton.Disabled = !(hasSelectedCategory && hasTemplate);
            AutoReplyResetButton.Disabled = !hasSelectedCategory;
            AutoReplyRuleEnableButton.Disabled = selectedCategory is not { HasAutoReply: true } || selectedCategory.AutoReplyEnabled;
            AutoReplyRuleDisableButton.Disabled = selectedCategory is not { HasAutoReply: true } || !selectedCategory.AutoReplyEnabled;
            TriageSaveButton.Disabled = !(hasSelectedCategory && hasKeywords);
            TriageResetButton.Disabled = !hasSelectedCategory;
            TriageRuleEnableButton.Disabled = selectedCategory is not { HasTriage: true } || selectedCategory.TriageEnabled;
            TriageRuleDisableButton.Disabled = selectedCategory is not { HasTriage: true } || !selectedCategory.TriageEnabled;
            PanicSaveButton.Disabled = panicTemplate.Length == 0 || string.Equals(panicTemplate, _currentPanicTemplate, StringComparison.Ordinal);
        }

        private void UpdateAhelpConfig(AhelpAdminConfigState state)
        {
            var currentCategory = SelectedCategory();

            _ahelpCategories.Clear();
            _ahelpCategories.AddRange(state.Categories.OrderBy(category => category.Name));
            _currentAutoReplyBotName = state.AutoReplyBotName;
            _currentPanicTemplate = state.PanicAutoReplyTemplate;

            RefreshCategoryOptions(currentCategory);
            RefreshAutoReplyList();
            RefreshTriageList();
            ApplyToggleState(AutoReplyEnableButton, AutoReplyDisableButton, state.AutoReplyEnabled);
            ApplyToggleState(TriageEnableButton, TriageDisableButton, state.TriageEnabled);
            ApplyToggleState(PanicEnableButton, PanicDisableButton, state.PanicAutoReplyEnabled);
            AutoReplyBotName.Text = state.AutoReplyBotName;
            PanicTemplate.TextRope = new Rope.Leaf(state.PanicAutoReplyTemplate);

            AutoReplyStateLabel.Text = Loc.GetString(
                state.AutoReplyEnabled
                    ? "admin-tools-ahelp-state-enabled"
                    : "admin-tools-ahelp-state-disabled");
            AutoReplyStateLabel.FontColorOverride = state.AutoReplyEnabled ? Color.LimeGreen : Color.Red;

            TriageStateLabel.Text = Loc.GetString(
                state.TriageEnabled
                    ? "admin-tools-triage-state-enabled"
                    : "admin-tools-triage-state-disabled");
            TriageStateLabel.FontColorOverride = state.TriageEnabled ? Color.LimeGreen : Color.Red;

            PanicStateLabel.Text = Loc.GetString(
                state.PanicAutoReplyEnabled
                    ? "admin-tools-panic-state-enabled"
                    : "admin-tools-panic-state-disabled");
            PanicStateLabel.FontColorOverride = state.PanicAutoReplyEnabled ? Color.LimeGreen : Color.Red;

            SyncSelectedCategoryEditors();
            UpdateAhelpEditorButtons();
        }

        private void RefreshCategoryOptions(string? preferredCategory)
        {
            _categoryList.Clear();
            AutoReplyCategory.Clear();

            foreach (var category in _ahelpCategories)
            {
                _categoryList.Add(category.Name);
                AutoReplyCategory.AddItem(category.Name, _categoryList.Count - 1);
            }

            if (_categoryList.Count == 0)
                return;

            var preferredIndex = preferredCategory == null
                ? 0
                : _categoryList.FindIndex(category => string.Equals(category, preferredCategory, StringComparison.OrdinalIgnoreCase));

            AutoReplyCategory.SelectId(preferredIndex >= 0 ? preferredIndex : 0);
        }

        private void RefreshAutoReplyList()
        {
            AutoReplyTemplatesList.Clear();
            foreach (var category in _ahelpCategories.Where(category => category.HasAutoReply))
            {
                var suffix = category.IsDefault ? string.Empty : Loc.GetString("admin-tools-ahelp-custom-tag");
                var enabledTag = category.AutoReplyEnabled
                    ? Loc.GetString("admin-tools-ahelp-enabled-tag")
                    : Loc.GetString("admin-tools-ahelp-disabled-tag");
                AutoReplyTemplatesList.AddItem($"{category.Name}{suffix}{enabledTag}  -  {TrimPreview(category.Template)}");
            }
        }

        private void RefreshTriageList()
        {
            TriageRulesList.Clear();
            foreach (var category in _ahelpCategories.Where(category => category.HasTriage))
            {
                var suffix = category.IsDefault ? string.Empty : Loc.GetString("admin-tools-ahelp-custom-tag");
                var enabledTag = category.TriageEnabled
                    ? Loc.GetString("admin-tools-ahelp-enabled-tag")
                    : Loc.GetString("admin-tools-ahelp-disabled-tag");
                TriageRulesList.AddItem($"{category.Name}{suffix}{enabledTag}  -  {TrimPreview(category.Keywords)}");
            }
        }

        private static string TrimPreview(string value)
        {
            var trimmed = value.Replace('\n', ' ').Trim();
            return trimmed.Length <= 80 ? trimmed : trimmed[..77] + "...";
        }

        private void ApplyToggleState(Button enableButton, Button disableButton, bool enabled)
        {
            enableButton.Pressed = enabled;
            disableButton.Pressed = !enabled;

            if (enabled)
                enableButton.AddStyleClass(StyleNano.StyleClassButtonColorGreen);
            else
                enableButton.RemoveStyleClass(StyleNano.StyleClassButtonColorGreen);

            disableButton.ModulateSelfOverride = enabled ? null : Color.Red;
        }

        private string? SelectedCategory()
        {
            if (_categoryList.Count == 0)
                return null;

            var selected = AutoReplyCategory.SelectedId;
            if (selected < 0 || selected >= _categoryList.Count)
                return null;

            return _categoryList[selected];
        }

        private AhelpAdminCategoryState? SelectedCategoryState()
        {
            var category = SelectedCategory();
            return category == null
                ? null
                : _ahelpCategories.FirstOrDefault(entry => string.Equals(entry.Name, category, StringComparison.OrdinalIgnoreCase));
        }

        private void OnAhelpCategorySelected(int id)
        {
            if (id < 0 || id >= _categoryList.Count)
                return;

            // OptionButton.ButtonOnPressed fires OnItemSelected without calling Select(),
            // so SelectedId is not updated on user interaction. Commit the selection here
            // so SyncSelectedCategoryEditors reads the correct category.
            AutoReplyCategory.SelectId(id);
            SyncSelectedCategoryEditors();
        }

        private void OnAutoReplyCategoryListSelected(int index)
        {
            var category = _ahelpCategories.Where(entry => entry.HasAutoReply).ElementAtOrDefault(index);
            SelectCategory(category?.Name);
        }

        private void OnTriageCategoryListSelected(int index)
        {
            var category = _ahelpCategories.Where(entry => entry.HasTriage).ElementAtOrDefault(index);
            SelectCategory(category?.Name);
        }

        private void SelectCategory(string? categoryName)
        {
            if (categoryName == null)
                return;

            var index = _categoryList.FindIndex(category => string.Equals(category, categoryName, StringComparison.OrdinalIgnoreCase));
            if (index < 0)
                return;

            AutoReplyCategory.SelectId(index);
            SyncSelectedCategoryEditors();
        }

        private void SyncSelectedCategoryEditors()
        {
            var category = SelectedCategoryState();

            if (category == null)
            {
                AhelpCategoryName.Text = string.Empty;
                AutoReplyTemplate.TextRope = Rope.Leaf.Empty;
                TriageKeywords.TextRope = Rope.Leaf.Empty;
                AutoReplyRuleStateLabel.Text = Loc.GetString("admin-tools-ahelp-rule-state-unselected");
                AutoReplyRuleStateLabel.FontColorOverride = null;
                TriageRuleStateLabel.Text = Loc.GetString("admin-tools-triage-rule-state-unselected");
                TriageRuleStateLabel.FontColorOverride = null;
                UpdateAhelpEditorButtons();
                return;
            }

            AhelpCategoryName.Text = category.Name;
            AutoReplyTemplate.TextRope = new Rope.Leaf(category.Template);
            TriageKeywords.TextRope = new Rope.Leaf(category.Keywords);
            AutoReplyRuleStateLabel.Text = Loc.GetString(
                category.AutoReplyEnabled
                    ? "admin-tools-ahelp-rule-state-enabled-category"
                    : "admin-tools-ahelp-rule-state-disabled-category",
                ("category", category.Name));
            AutoReplyRuleStateLabel.FontColorOverride = category.AutoReplyEnabled ? Color.LimeGreen : Color.Red;
            TriageRuleStateLabel.Text = Loc.GetString(
                category.TriageEnabled
                    ? "admin-tools-triage-rule-state-enabled-category"
                    : "admin-tools-triage-rule-state-disabled-category",
                ("category", category.Name));
            TriageRuleStateLabel.FontColorOverride = category.TriageEnabled ? Color.LimeGreen : Color.Red;
            UpdateAhelpEditorButtons();
        }

        private void CreateCustomVote()
        {
            var title = CustomVoteTitle.Text.Trim();
            if (title.Length == 0)
                return;

            var options = CustomVoteOptions.Text
                .Split(';')
                .Select(x => x.Trim())
                .Where(x => x.Length > 0)
                .ToArray();

            if (options.Length < 2)
                return;

            var escapedTitle = CommandParsing.Escape(title);
            var escapedOptions = string.Join(" ", options.Select(x => $"\"{CommandParsing.Escape(x)}\""));
            _console.ExecuteCommand($"customvote \"{escapedTitle}\" {escapedOptions}");
        }

