        private void OnSubTabChanged(int tabIndex)
        {
            if ((SubTabIndex) tabIndex == SubTabIndex.Objects)
                ObjectsTabControl.RefreshObjectList();

            if ((SubTabIndex) tabIndex == SubTabIndex.Statistics)
                RefreshStatistics();
        }

        private void RefreshStatistics()
        {
            StatisticsUpdatedLabel.Text = Loc.GetString("admin-tools-statistics-updated-loading");
            _bwoinkSystem.RequestAdminStatistics();
        }

        private void UpdateStatistics(AdminStatisticsResponseMessage message)
        {
            if (!string.IsNullOrEmpty(message.Error) || message.Snapshot == null)
            {
                StatisticsOnlineLabel.Text = Loc.GetString("admin-tools-statistics-error");
                StatisticsLagLabel.Text = string.Empty;
                StatisticsRoundLabel.Text = string.Empty;
                StatisticsUptimeLabel.Text = string.Empty;
                StatisticsRolesList.Clear();
                StatisticsAntagsList.Clear();
                StatisticsUpdatedLabel.Text = Loc.GetString("admin-tools-statistics-updated-error");
                return;
            }

            var snapshot = message.Snapshot;
            StatisticsOnlineLabel.Text = Loc.GetString("admin-tools-statistics-online-value", ("count", snapshot.OnlinePlayers));
            StatisticsLagLabel.Text = Loc.GetString(
                "admin-tools-statistics-lag-value",
                ("avg", snapshot.AveragePingMs),
                ("max", snapshot.MaxPingMs),
                ("high", snapshot.HighPingPlayers));
            StatisticsRoundLabel.Text = Loc.GetString(
                "admin-tools-statistics-round-value",
                ("roundId", snapshot.RoundId),
                ("runLevel", snapshot.RunLevel),
                ("duration", FormatDuration(snapshot.RoundDuration)));
            StatisticsUptimeLabel.Text = Loc.GetString(
                "admin-tools-statistics-uptime-value",
                ("uptime", FormatDuration(snapshot.ServerUptime)));

            _allRoleSlots = snapshot.RoleSlots;
            RolesSearchBox.Clear();
            ApplyRolesFilter();

            StatisticsAntagsList.Clear();
            if (snapshot.Antags.Length == 0)
            {
                StatisticsAntagsList.AddItem(Loc.GetString("admin-tools-statistics-antags-none"));
            }
            else
            {
                foreach (var antag in snapshot.Antags)
                {
                    StatisticsAntagsList.AddItem(antag);
                }
            }

            StatisticsUpdatedLabel.Text = Loc.GetString(
                "admin-tools-statistics-updated-now",
                ("time", DateTime.Now.ToString("HH:mm:ss")));
        }

        private static string FormatDuration(TimeSpan duration)
        {
            var totalHours = (int) duration.TotalHours;
            return $"{totalHours:00}:{duration.Minutes:00}:{duration.Seconds:00}";
        }

        private void ApplyRolesFilter()
        {
            var searchText = RolesSearchBox.Text.Trim().ToLowerInvariant();
            StatisticsRolesList.Clear();

            foreach (var role in _allRoleSlots)
            {
                // Filter by search text
                if (!string.IsNullOrEmpty(searchText) && 
                    !role.RoleName.ToLowerInvariant().Contains(searchText))
                    continue;

                var roleText = role.OpenSlots == null
                    ? Loc.GetString(
                        "admin-tools-statistics-role-unlimited",
                        ("name", role.RoleName),
                        ("taken", role.TakenSlots))
                    : Loc.GetString(
                        "admin-tools-statistics-role-finite",
                        ("name", role.RoleName),
                        ("taken", role.TakenSlots),
                        ("open", role.OpenSlots.Value));

                StatisticsRolesList.AddItem(roleText);
            }
        }

        private void UpdateSavedCommandButtons()
        {
            var hasName = SavedCommandName.Text.Trim().Length > 0;
            var hasCommand = Rope.Collapse(SavedCommandText.TextRope).Trim().Length > 0;
            SavedCommandSaveButton.Disabled = !(hasName && hasCommand);
            SavedCommandRunButton.Disabled = !hasCommand && GetSelectedSavedIndex() < 0;
            SavedCommandDeleteButton.Disabled = GetSelectedSavedIndex() < 0 && !hasName;
        }

